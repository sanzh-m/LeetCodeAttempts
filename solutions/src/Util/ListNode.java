package Util;

//Everything is public because on Leetcode Listnode is assumed to have accessible instance variables and constructors
public class ListNode {
    public int val;
    public ListNode next;
    public ListNode() {}
    public ListNode(int val) { this.val = val; }
    public ListNode(int val, ListNode next) { this.val = val; this.next = next; }
}
